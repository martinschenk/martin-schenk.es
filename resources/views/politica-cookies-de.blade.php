		<h2>1. Was ist ein Cookie?</h2>
		
		<p>Ein Cookie ist eine Textdatei, die auf dem Gerät des Benutzers heruntergeladen wird, wenn er auf bestimmte Webseiten zugreift. Ihr Zweck ist es, Informationen über die von diesem Gerät ausgeführte Navigation zu speichern und wiederherzustellen.</p>
		
		<p>Auf unserer Website verwenden wir Cookies ausschließlich, um die optimale Funktionsweise und die wesentlichen Funktionen der Seite zu gewährleisten. Wir verwenden keine Tracking-Cookies, um Daten über Ihre Navigation zu Werbezwecken oder Analysen zu sammeln.</p>
		
		<h2>2. Welche Cookies werden auf dieser Website verwendet?</h2>
		
		<h4>Session-Cookies</h4>
		
		<p>Diese Cookies ermöglichen es den Benutzern, auf der Website erkannt zu werden, sodass jede vorgenommene Änderung, jeder ausgewählte Artikel oder eingegebene Daten von einer Seite zur anderen erinnert werden. Diese Cookies werden automatisch von der Festplatte gelöscht, wenn Sie Ihren Browser schließen.</p>
		
		<h4>Links zu Websites Dritter</h4>
		
		<p>Unsere Website kann Links zu anderen Websites enthalten. Wir haben keine Kontrolle über und sind nicht verantwortlich für die Cookies, die von diesen externen Seiten verwendet werden. Für weitere Informationen über deren Cookie-Richtlinien empfehlen wir, deren jeweilige Seiten zu überprüfen.</p>
		
